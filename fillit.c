void	exit_msg(int i)
{
	if (i == 0)
	{
		write(1, "error\n", 6);
		exit(EXIT_SUCCESS);
	}
	if (i == 1)
	{
		write(1, "usage: ./fillit source_file\n", 28);
		exit(EXIT_FAILURE);
	}
	write(1, "error\n", 6);
	exit(EXIT_FAILURE);
}

int		main(int argc, char *argv[])
{
	t_tt		tet[27];
	t_gril		grille;
	int			ret;
	int			i;

	if (argc != 2)
		exit_msg(1);
	ft_init();
	read_fic(argv[1], tet);
	cal_si(tet);
	ret = 1;
	i = tet[26][1];
	while (ret == 1)
	{
		g_init(grille, tet[26][1]);
		ret = resolve(grille, tet);
		if (ret == 1)
			(tet[26][1])++;
	}
	if (i != tet[26][1])
		;
	if (ret != 0)
		exit_msg(4);
	affiche(tet);
	return (0);
}
